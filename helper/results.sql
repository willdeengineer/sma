-- Run duur berekenen met row counts
WITH run_durations AS (
    SELECT 
        'CDC_SQL_DB' AS DATABASE_NAME,
        RUN_ID,
        START_TS,
        END_TS,
        DATEDIFF(MILLISECOND, START_TS, END_TS) / 1000.0 AS DURATION
    FROM CDC_SQL_DB.LOGGING.RUN_LOG
    
    UNION ALL
    
    SELECT 
        'CDC_PYTHON_DB' AS DATABASE_NAME,
        RUN_ID,
        START_TS,
        END_TS,
        DATEDIFF(MILLISECOND, START_TS, END_TS) / 1000.0 AS DURATION
    FROM CDC_PYTHON_DB.LOGGING.RUN_LOG
),
row_counts AS (
    SELECT RUN_ID, SUM(ROWS_INSERTED) AS TOTAL_ROWS_INSERTED
    FROM CDC_SQL_DB.LOGGING.RUN_LOG
    GROUP BY RUN_ID
    
    UNION ALL
    
    SELECT RUN_ID, SUM(ROWS_INSERTED) AS TOTAL_ROWS_INSERTED
    FROM CDC_PYTHON_DB.LOGGING.RUN_LOG
    GROUP BY RUN_ID
)
SELECT 
    rd.RUN_ID,
    MAX(CASE WHEN DATABASE_NAME = 'CDC_SQL_DB' THEN DURATION END) AS SQL_DURATION,
    MAX(CASE WHEN DATABASE_NAME = 'CDC_PYTHON_DB' THEN DURATION END) AS PYTHON_DURATION,
    MAX(CASE WHEN DATABASE_NAME = 'CDC_PYTHON_DB' THEN DURATION END) - 
    MAX(CASE WHEN DATABASE_NAME = 'CDC_SQL_DB' THEN DURATION END) AS DURATION_DIFFERENCE,
    SUM(rc.TOTAL_ROWS_INSERTED) AS TOTAL_ROWS_INSERTED
FROM run_durations rd
LEFT JOIN row_counts rc ON rd.RUN_ID = rc.RUN_ID
GROUP BY rd.RUN_ID
ORDER BY rd.RUN_ID ASC;